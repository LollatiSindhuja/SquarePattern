import java.util.*;
public class Pattern2 {
    public static void main(String[] args) {
        int numlines=5;
        for(int i=1;i<=numlines;i++){
            if(i%2!=0){
                System.out.println("* # * # *");
            }else{
                System.out.println("# # # # #");
            }
        }

    }
    
}
